function normalize(text) {
	const filters = [
		/* [،؛].. -> [،؛] */
		[/([،؛])+/g, '$1'],
		
		/* [،؛.!؟:] -> [،؛.!؟:]\s */
		[/ *([،؛\.!؟:]) */g, '$1 '],

		/* !\s! -> !! */
		[/(! +){2}/g, '!!'],

		/* ?\s? -> ?? */
		[/(؟ +){2}/g, '؟؟'],

		/* :\s: -> :: */
		[/(: +){2}/g, '::'],
		
		/* .\s. -> .. */
		[/(\. +){2}/g, '..'],

		/* .\w -> . \w */
		[/\.(\w)/g, '. $1'],

		/* .... -> ... . */
		[/(\.{3,})\./g, '$1 .'],

		/* [،؛!؟:]\w -> [،؛!؟:]\s\w */
		[/ *([،؛!؟:])(\w)/g, '$1 $2'],
		
		/* (( -> « */
		[/\(( *\()+ */g, '«'],

		/* )) -> » */
		[/ *\)( *\))+/g, '»'],

		/* << -> « */
		[/<( *<)+ */g, '«'],

		/* >> -> » */
		[/ *>( *>)+/g, '»'],

		/* :« -> : « */
		[/: *«/g, ': «'],

		/* «\s -> « */
		[/« +/g, '«'],

		/* \s» -> » */
		[/ +»/g, '»'],

		/* [0-9][.،!؟؛]\s[0-9] -> [0-9][.،!؟؛][0-9] */
		[/([0-9])([.،!؟؛]) +([0-9])/g, '$1$2$3'],

		/* [٠-٩][.،!؟؛]\s[٠-٩] -> [٠-٩][.،!؟؛][٠-٩] */
		[/([٠-٩])([.،!؟؛]) +([٠-٩])/g, '$1$2$3'],

		/* [۰-۹][.،!؟؛]\s[۰-۹] -> [۰-۹][.،!؟؛][۰-۹] */
		[/([۰-۹])([.،!؟؛]) +([۰-۹])/g, '$1$2$3'],

		/* \w\s..\w -> \w\s\w */
		[/(\w) {2,}(\w)/g, '$1 $2'],

		/* \s..$ -> $ */
		[/ +$/gm, ''],

		/* ^\s.. -> ^ */
		[/^ +/gm, ''],
	]
	
	/* Apply Filters */
	for(const filter of filters)
		text = text.replace(filter[0], filter[1])
	
	/* Trim Text */
	text = text.trim()

	return text
}
